def game_events(n: int) -> str:
    for i in range(n):
        if data[i]["event_type"] == "death":
            message = "died"
        elif data[i]["event_type"] == "level_up":
            message = "leveled up"
        elif data[i]["event_type"] == "login":
            message = "logged in"
        elif data[i]["event_type"] == "logout":
            message = "logged out"
        elif data[i]["event_type"] == "kill":
            message = "killed a monster"
        elif data[i]["event_type"] == "item_found":
            message = "found treasure"
        else:
            message = "event occurred"

        event_data = data[i]
        output = f"Event {event_data['id']}: Player {event_data['player']} (level {event_data['data']['level']}) {message}"
        yield output


def event_count(data: dict, events: str) -> int:
    count = 0
    if events == "level":
        for event in data:
            if event["data"]["level"] >= 10:
                count += 1
    else:
        for event in data:
            if event["event_type"] == events:
                count += 1
    return count

